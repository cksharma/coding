    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        state = jTextField6.getText();
        constituency = jTextField7.getText();
        if(state != null && state.length() > 0)
        {
            Election.votersFromState(state);
        }
        else if(constituency != null && constituency.length() > 0)
        {
            Election.votersFromConstituency(constituency);
        }
        else
        {
        	JOptionPane.showMessageDialog(null, "Please enter State or Constituency");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

