    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        voterName = jTextField1.getText();
        gender = jTextField2.getText();
        ck = jTextField3.getText();
        constituency = jTextField4.getText();
        state = jTextField5.getText();
        if(voterName.length() == 0 || gender.length() == 0 || ck.length() == 0 || constituency.length() == 0 || state.length() == 0)
        {
        	JOptionPane.showMessageDialog(null, "Please enter all the voter details.");
        }
        else
        {
        	Election.votersAdd(voterName, gender, ck, constituency, state);
        }	
    }//GEN-LAST:event_jButton1ActionPerformed

